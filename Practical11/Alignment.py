def dis(seq1,seq2):
    edit_distance	=	0 #set	initial	distance	as	zero
    for	i	in	range(len(seq1)): #compare	each	amino	acid
        if seq1[i] != '\n':
            a=amino.index(seq1[i])
            b=amino.index(seq2[i])
            edit_distance=edit_distance+blosum[a][b]
    return edit_distance
def per(seq1,seq2):
    edit_distance	=	0 
    for	i	in	range(len(seq1)): #compare	each	amino	acid
	    if seq1[i] != seq2[i] :		
	        edit_distance	+=	1
    percentage= 1-edit_distance/len(seq1)
    return percentage                
